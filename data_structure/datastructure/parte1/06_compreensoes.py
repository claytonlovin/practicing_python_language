# Introdução às Compreensões em Python

# Este arquivo aborda as compreensões em Python, que são uma maneira concisa de criar listas, dicionários e conjuntos. 
# As compreensões permitem que você escreva código mais limpo e legível, reduzindo a necessidade de loops explícitos.

# Exemplos de compreensões:

# 1. Compreensão de lista
# lista = [x**2 for x in range(10)]  # Cria uma lista dos quadrados de 0 a 9

# 2. Compreensão de dicionário
# dicionario = {x: x**2 for x in range(10)}  # Cria um dicionário com números e seus quadrados

# 3. Compreensão de conjunto
# conjunto = {x**2 for x in range(10)}  # Cria um conjunto dos quadrados de 0 a 9 (sem duplicatas)

# As compreensões podem incluir condições:
# lista_filtrada = [x for x in range(10) if x % 2 == 0]  # Lista apenas com números pares

# As compreensões são uma ferramenta poderosa para simplificar a criação de coleções em Python.